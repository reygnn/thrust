package com.github.reygnn.thrust.domain.level

/**
 * Endless-Mode-Schwierigkeit. Die Werte hier sind die Generierungs-Parameter,
 * die der [LevelGenerator] ausliest. Story-Mode nutzt diese Klasse nicht.
 */
